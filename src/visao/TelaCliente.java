    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String [] colunas = new String []{"ID", "Nome", "Endere√ßo", "Telefone", "Celular"};
        conex.conexao();
        conex.executaSql(Sql);
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("id_cliente"),conex.rs.getString("nome_cliente"),conex.rs.getString("endereco_cliente"),conex.rs.getString("telefone_cliente"),conex.rs.getString("celular_cliente")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane,"Busque por outro cliente para preencher tabela");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tabelacliente.setModel(modelo);
        tabelacliente.getColumnModel().getColumn(0).setPreferredWidth(59);
        tabelacliente.getColumnModel().getColumn(0).setResizable(false);
        tabelacliente.getColumnModel().getColumn(1).setPreferredWidth(325);
        tabelacliente.getColumnModel().getColumn(1).setResizable(false);
        tabelacliente.getColumnModel().getColumn(2).setPreferredWidth(275);
        tabelacliente.getColumnModel().getColumn(2).setResizable(false);
        tabelacliente.getColumnModel().getColumn(3).setPreferredWidth(120);
        tabelacliente.getColumnModel().getColumn(3).setResizable(false);
        tabelacliente.getColumnModel().getColumn(4).setPreferredWidth(130);
        tabelacliente.getColumnModel().getColumn(4).setResizable(false);
        tabelacliente.getTableHeader().setReorderingAllowed(false);
        tabelacliente.setAutoResizeMode(tabelacliente.AUTO_RESIZE_OFF);
        tabelacliente.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }
    
