    public void preencherTabelaCliente(String Sql) {
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"ID", "Nome"};
        conex.conexao();
        conex.executaSql(Sql);
        try {
            conex.rs.first();
            do {
                dados.add(new Object[]{conex.rs.getInt("id_cliente"), conex.rs.getString("nome_cliente")});
            } while (conex.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Busque por outro cliente para preencher tabela");
        }
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        tabelapesquisa.setModel(modelo);
        tabelapesquisa.getColumnModel().getColumn(0).setPreferredWidth(100);
        tabelapesquisa.getColumnModel().getColumn(0).setResizable(false);
        tabelapesquisa.getColumnModel().getColumn(1).setPreferredWidth(625);
        tabelapesquisa.getColumnModel().getColumn(1).setResizable(false);
        tabelapesquisa.getTableHeader().setReorderingAllowed(false);
        tabelapesquisa.setAutoResizeMode(tabelapesquisa.AUTO_RESIZE_OFF);
        tabelapesquisa.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conex.desconecta();
    }

