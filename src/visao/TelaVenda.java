    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (nomeproduto.getText().isEmpty()) {
            nomeproduto.requestFocus();
            JOptionPane.showMessageDialog(rootPane, "Busque pelo produto");
        } else {
            try {
                int idVenda, quantidadee = 0;
                conex.conexao();
                conex.executaSql("select *from produtos where nome_produto = '" + nomeproduto.getText() + "'");
                conex.rs.first();
                quantidadee = conex.rs.getInt("quantidade_produto");
                if (quantidadee >= Integer.parseInt(quantidade.getText())) {
                    mod.setNomeProduto(nomeproduto.getText());
                    mod.setQuantidadeItem(Integer.parseInt(quantidade.getText()));
                    mod.setIdVenda(codVenda);
                    control.adicionaItem(mod);
                    preencherItensVenda("select *from produtos inner join itens_venda_produto "
                            + "on produtos.id_produto = itens_venda_produto.id_produto inner join venda "
                            + "on venda.id_venda = itens_venda_produto.id_venda where venda.id_venda = " + codVenda);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "A quantidade desejada não está disponivel no estoque!");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, "Eroo ao pesquisar quantidade\nEroo: " + ex);
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(rootPane, "Quantidade Invalida", "Erro", 1);
            }
            nomeproduto.setText("");
            quantidade.setText("");
            valor.setText("");
            preencherTabelaProduto("select *from produtos where nome_produto like'%" + nomeproduto.getText() + "%'");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

