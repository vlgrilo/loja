    public void SomaProduto() {
        total = 0;
        int qtd = 0;
        float valor = 0;
        conex.executaSql("select *from itens_venda_produto inner join produtos "
                + "on itens_venda_produto.id_produto = produtos.id_produto where id_venda = " + codVenda);
        try {
            while (conex.rs.next()) {
                qtd = conex.rs.getInt("quantidade");
                valor = conex.rs.getFloat("preco_produto");
                total = total + conex.rs.getFloat("preco_produto") * conex.rs.getInt("quantidade");
            }
            valortotal.setText(String.valueOf(total));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Eroo na soma do total\nErro: " + ex);
        }
    }

